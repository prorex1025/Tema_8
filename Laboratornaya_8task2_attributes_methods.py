class Car:
    """
    –ö–ª–∞—Å—Å Car —Å –∏–Ω–∫–∞–ø—Å—É–ª–∏—Ä–æ–≤–∞–Ω–Ω—ã–º–∏ –∞—Ç—Ä–∏–±—É—Ç–∞–º–∏

    –ê—Ç—Ä–∏–±—É—Ç—ã:
        _make (str): –∑–∞—â–∏—â–µ–Ω–Ω—ã–π –∞—Ç—Ä–∏–±—É—Ç –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç–µ–ª—è
        __model (str): –ø—Ä–∏–≤–∞—Ç–Ω—ã–π –∞—Ç—Ä–∏–±—É—Ç –º–æ–¥–µ–ª–∏
        __vin (str): –ø—Ä–∏–≤–∞—Ç–Ω—ã–π VIN-–Ω–æ–º–µ—Ä
    """

    def __init__(self, make, model, vin):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä —Å –∏–Ω–∫–∞–ø—Å—É–ª–∏—Ä–æ–≤–∞–Ω–Ω—ã–º–∏ –∞—Ç—Ä–∏–±—É—Ç–∞–º–∏

        Args:
            make (str): –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç–µ–ª—å
            model (str): –º–æ–¥–µ–ª—å
            vin (str): VIN-–Ω–æ–º–µ—Ä –∞–≤—Ç–æ–º–æ–±–∏–ª—è
        """
        self._make = make          # –ó–∞—â–∏—â–µ–Ω–Ω—ã–π –∞—Ç—Ä–∏–±—É—Ç (–æ–¥–Ω–æ –ø–æ–¥—á–µ—Ä–∫–∏–≤–∞–Ω–∏–µ)
        self.__model = model       # –ü—Ä–∏–≤–∞—Ç–Ω—ã–π –∞—Ç—Ä–∏–±—É—Ç (–¥–≤–∞ –ø–æ–¥—á–µ—Ä–∫–∏–≤–∞–Ω–∏—è)
        self.__vin = vin           # –ü—Ä–∏–≤–∞—Ç–Ω—ã–π VIN-–Ω–æ–º–µ—Ä
        self.__mileage = 0         # –ü—Ä–∏–≤–∞—Ç–Ω—ã–π –ø—Ä–æ–±–µ–≥
        self._is_running = False   # –ó–∞—â–∏—â–µ–Ω–Ω—ã–π —Å—Ç–∞—Ç—É—Å –¥–≤–∏–≥–∞—Ç–µ–ª—è

    # –ü—É–±–ª–∏—á–Ω—ã–µ –º–µ—Ç–æ–¥—ã –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ –ø—Ä–∏–≤–∞—Ç–Ω—ã–º –¥–∞–Ω–Ω—ã–º
    def get_model(self):
        """–ü—É–±–ª–∏—á–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –º–æ–¥–µ–ª–∏"""
        return self.__model

    def get_vin(self):
        """–ü—É–±–ª–∏—á–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è VIN (—Ç–æ–ª—å–∫–æ —á–∞—Å—Ç–∏—á–Ω–æ)"""
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–æ–ª—å–∫–æ —á–∞—Å—Ç—å VIN –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
        return f"***{self.__vin[-6:]}"

    def get_mileage(self):
        """–ü—É–±–ª–∏—á–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø—Ä–æ–±–µ–≥–∞"""
        return self.__mileage

    def set_mileage(self, new_mileage):
        """
        –ü—É–±–ª–∏—á–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –ø—Ä–æ–±–µ–≥–∞ —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π

        Args:
            new_mileage (float): –Ω–æ–≤—ã–π –ø—Ä–æ–±–µ–≥
        """
        if new_mileage >= self.__mileage:
            self.__mileage = new_mileage
            print(f"üìä –ü—Ä–æ–±–µ–≥ –æ–±–Ω–æ–≤–ª–µ–Ω: {new_mileage} –∫–º")
        else:
            print("‚ùå –û—à–∏–±–∫–∞: –ø—Ä–æ–±–µ–≥ –Ω–µ –º–æ–∂–µ—Ç —É–º–µ–Ω—å—à–∞—Ç—å—Å—è!")

    # –ú–µ—Ç–æ–¥—ã —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –∞–≤—Ç–æ–º–æ–±–∏–ª–µ–º
    def start_engine(self):
        """–ó–∞–ø—É—Å–∫ –¥–≤–∏–≥–∞—Ç–µ–ª—è"""
        self._is_running = True
        print(f"üöó {self._make} {self.__model}: –¥–≤–∏–≥–∞—Ç–µ–ª—å –∑–∞–ø—É—â–µ–Ω")

    def stop_engine(self):
        """–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –¥–≤–∏–≥–∞—Ç–µ–ª—è"""
        self._is_running = False
        print(f"üöó {self._make} {self.__model}: –¥–≤–∏–≥–∞—Ç–µ–ª—å –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω")

    def drive(self, distance):
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è –¥–≤–∏–∂–µ–Ω–∏—è –∞–≤—Ç–æ–º–æ–±–∏–ª—è —Å –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ–º –ø—Ä–æ–±–µ–≥–∞

        Args:
            distance (float): —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –≤ –∫–º
        """
        if self._is_running:
            new_mileage = self.__mileage + distance
            self.set_mileage(new_mileage)
            print(f"üöó –ü—Ä–æ–µ—Ö–∞–ª–∏ {distance} –∫–º")
        else:
            print("‚ùå –°–Ω–∞—á–∞–ª–∞ –∑–∞–ø—É—Å—Ç–∏—Ç–µ –¥–≤–∏–≥–∞—Ç–µ–ª—å!")

    def get_info(self):
        """–ü—É–±–ª–∏—á–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ–± –∞–≤—Ç–æ–º–æ–±–∏–ª–µ"""
        status = "–∑–∞–≤–µ–¥–µ–Ω" if self._is_running else "–∑–∞–≥–ª—É—à–µ–Ω"
        return (f"–ê–≤—Ç–æ–º–æ–±–∏–ª—å: {self._make} {self.__model} | "
                f"VIN: {self.get_vin()} | "
                f"–ü—Ä–æ–±–µ–≥: {self.__mileage} –∫–º | "
                f"–°–æ—Å—Ç–æ—è–Ω–∏–µ: {status}")

