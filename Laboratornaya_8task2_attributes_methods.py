    print(f"\n3. –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –¥–æ—Å—Ç—É–ø —á–µ—Ä–µ–∑ –≥–µ—Ç—Ç–µ—Ä—ã:")
