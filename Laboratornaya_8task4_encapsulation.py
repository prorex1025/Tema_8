class Rectangle(Shape):
    """
    –ö–ª–∞—Å—Å Rectangle (–ü—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫) –Ω–∞—Å–ª–µ–¥—É–µ—Ç—Å—è –æ—Ç Shape
    """

    def __init__(self, width, height):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞

        Args:
            width (float): —à–∏—Ä–∏–Ω–∞ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞
            height (float): –≤—ã—Å–æ—Ç–∞ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞
        """
        self.width = width
        self.height = height

    def area(self):
        """–í—ã—á–∏—Å–ª—è–µ—Ç –ø–ª–æ—â–∞–¥—å –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞"""
        return self.width * self.height

    def perimeter(self):
        """–í—ã—á–∏—Å–ª—è–µ—Ç –ø–µ—Ä–∏–º–µ—Ç—Ä –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞"""
        return 2 * (self.width + self.height)

    def get_info(self):
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–µ"""
        return f"{super().get_info()} | –®–∏—Ä–∏–Ω–∞: {self.width} | –í—ã—Å–æ—Ç–∞: {self.height}"

class Circle(Shape):
    """
    –ö–ª–∞—Å—Å Circle (–ö—Ä—É–≥) –Ω–∞—Å–ª–µ–¥—É–µ—Ç—Å—è –æ—Ç Shape
    """

    def __init__(self, radius):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –∫—Ä—É–≥–∞

        Args:
            radius (float): —Ä–∞–¥–∏—É—Å –∫—Ä—É–≥–∞
        """
        self.radius = radius

    def area(self):
        """–í—ã—á–∏—Å–ª—è–µ—Ç –ø–ª–æ—â–∞–¥—å –∫—Ä—É–≥–∞"""
        return 3.14159 * self.radius ** 2

    def perimeter(self):
        """–í—ã—á–∏—Å–ª—è–µ—Ç –¥–ª–∏–Ω—É –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏ (–ø–µ—Ä–∏–º–µ—Ç—Ä –∫—Ä—É–≥–∞)"""
        return 2 * 3.14159 * self.radius

    def get_info(self):
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –∫—Ä—É–≥–µ"""
        return f"{super().get_info()} | –†–∞–¥–∏—É—Å: {self.radius}"

class Triangle(Shape):
    """
    –ö–ª–∞—Å—Å Triangle (–¢—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫) –Ω–∞—Å–ª–µ–¥—É–µ—Ç—Å—è –æ—Ç Shape
    –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –∫–ª–∞—Å—Å –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏ –ø–æ–ª–∏–º–æ—Ä—Ñ–∏–∑–º–∞
    """

    def __init__(self, side1, side2, side3):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞

        Args:
            side1, side2, side3 (float): —Å—Ç–æ—Ä–æ–Ω—ã —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞
        """
        self.side1 = side1
        self.side2 = side2
        self.side3 = side3

    def area(self):
        """–í—ã—á–∏—Å–ª—è–µ—Ç –ø–ª–æ—â–∞–¥—å —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –ø–æ —Ñ–æ—Ä–º—É–ª–µ –ì–µ—Ä–æ–Ω–∞"""
        s = self.perimeter() / 2
        return (s * (s - self.side1) * (s - self.side2) * (s - self.side3)) ** 0.5

    def perimeter(self):
        """–í—ã—á–∏—Å–ª—è–µ—Ç –ø–µ—Ä–∏–º–µ—Ç—Ä —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞"""
        return self.side1 + self.side2 + self.side3

    def get_info(self):
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–µ"""
        return f"{super().get_info()} | –°—Ç–æ—Ä–æ–Ω—ã: {self.side1}, {self.side2}, {self.side3}"

